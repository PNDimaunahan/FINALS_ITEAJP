        if (selectedRow == -1) {
            JOptionPane.showMessageDialog(this, "Please select a product to add to the order list.");
            return;
        }

        DefaultTableModel productTableModel = (DefaultTableModel) jTable1.getModel();
        DefaultTableModel orderTableModel = (DefaultTableModel) jTable2.getModel();

        String productName = (String) productTableModel.getValueAt(selectedRow, 0);
        int quantity = 1; // You can modify this to allow the user to specify quantity
        float price = (float) productTableModel.getValueAt(selectedRow, 4); // Assuming price is in the 5th column

        orderTableModel.addRow(new Object[]{productName, quantity, price});

        // Update total price
        updateTotalPrice();
