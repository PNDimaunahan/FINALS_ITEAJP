    private void completeButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_completeButtonActionPerformed
        // Calculate total price
        updateTotalPrice();

        // Retrieve payment amount from the paymentField
        String paymentStr = paymentField.getText();
        if (paymentStr.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please enter payment amount.");
            return;
        }

        float payment;
        try {
            payment = Float.parseFloat(paymentStr);
        } catch (NumberFormatException ex) {
            JOptionPane.showMessageDialog(this, "Invalid payment amount. Please enter a valid number.");
            return;
        }

        // Compare payment with total price
        float totalPrice = getTotalPrice();
        if (payment < totalPrice) {
            JOptionPane.showMessageDialog(this, "Insufficient payment. Please enter a higher amount.");
            return;
        }

        // Set the order status as "Not Complete"
        String status = "Not Complete";

        // Complete the order
        try {
            // Retrieve order details from the order table
            DefaultTableModel orderTableModel = (DefaultTableModel) jTable2.getModel();
            int rowCount = orderTableModel.getRowCount();

            // Get the current date as the order date
            Date orderDate = new Date();

            float totalAmount = updateTotalPrice();
            // Call OrderService to add order
            OrderService orderService = new OrderService();

            // Create an Order object and add it to the database
            Order order = new Order(orderDate, totalAmount, status);
            orderService.addOrder(order);

            // Insert order details
            for (int i = 0; i < rowCount; i++) {
                String productName = (String) orderTableModel.getValueAt(i, 0);
                int quantity = (int) orderTableModel.getValueAt(i, 1);
                float price = (float) orderTableModel.getValueAt(i, 2);

                int productID = ProductService.getProductIDByName(productName);
                if (productID != -1) {
                    OrderDetails orderDetails = new OrderDetails(order.getOrderID(), productID, quantity, price);
                    orderService.addOrderDetails(orderDetails);
                } else {
                    System.out.println("Product not found: " + productName);
                }
            }


            // Display a confirmation message
            JOptionPane.showMessageDialog(this, "Order completed successfully!");

            // Clear the order list
            clearOrder();

        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, "An error occurred while processing the order.");
            ex.printStackTrace();
        }

    }//GEN-LAST:event_completeButtonActionPerformed

