    public float updateTotalPrice() {
    DefaultTableModel orderTableModel = (DefaultTableModel) jTable2.getModel();
    float totalPrice = 0.0f;
    for (int i = 0; i < orderTableModel.getRowCount(); i++) {
        float price = (float) orderTableModel.getValueAt(i, 2);
        int quantity = (int) orderTableModel.getValueAt(i, 1);
        totalPrice += price * quantity;
    }
    jLabel4.setText("Total Price: " + totalPrice);
    return totalPrice; // Return the calculated total price
}

private void clearOrder() {
    DefaultTableModel orderTableModel = (DefaultTableModel) jTable2.getModel();
    orderTableModel.setRowCount(0); // Clearing the table
    jLabel4.setText("Total Price: 0"); // Resetting total price label
}

private float getTotalPrice() {
    String totalPriceStr = jLabel4.getText().substring(13); // Extracting the price part from the label text
    return Float.parseFloat(totalPriceStr);
}

