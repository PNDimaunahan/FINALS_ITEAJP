    // Load orders into the table
    private void loadOrders() {
           try {
               List<Order> orders = orderService.getAllOrders();
               DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
               model.setRowCount(0); // Clear existing rows

               for (Order order : orders) {
                   String formattedDate = dateFormat.format(order.getOrderDate());
                   model.addRow(new Object[]{order.getOrderID(), formattedDate, order.getTotalAmount(), order.getStatus()});
               }
           } catch (SQLException e) {
               JOptionPane.showMessageDialog(this, "Error loading orders: " + e.getMessage(), "Database Error", JOptionPane.ERROR_MESSAGE);
           }
       }
