    private void completeOdButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_completeOdButtonActionPerformed
        int selectedRow = jTable1.getSelectedRow();
        if (selectedRow != -1) {
            DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
            int orderId = (int) model.getValueAt(selectedRow, 0);
            try {
                orderService.completeOrder(orderId);
                loadOrders(); // Refresh the order list
            } catch (SQLException e) {
                JOptionPane.showMessageDialog(this, "Error completing order: " + e.getMessage(), "Database Error", JOptionPane.ERROR_MESSAGE);
            }
        } else {
            JOptionPane.showMessageDialog(this, "Please select an order to complete", "Input Error", JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_completeOdButtonActionPerformed

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String orderIdStr = jTextField1.getText().trim();
            if (!orderIdStr.isEmpty()) {
                try {
                    int orderId = Integer.parseInt(orderIdStr);
                    Order order = orderService.getOrderById(orderId);
                    if (order != null) {
                        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
                        model.setRowCount(0); // Clear existing rows
                        String formattedDate = dateFormat.format(order.getOrderDate());
                        model.addRow(new Object[]{order.getOrderID(), formattedDate, order.getTotalAmount(), order.getStatus()});
                    } else {
                        JOptionPane.showMessageDialog(this, "Order not found", "Search Result", JOptionPane.INFORMATION_MESSAGE);
                    }
                } catch (NumberFormatException e) {
                    JOptionPane.showMessageDialog(this, "Invalid order ID format", "Input Error", JOptionPane.ERROR_MESSAGE);
                } catch (SQLException e) {
                    JOptionPane.showMessageDialog(this, "Error searching order: " + e.getMessage(), "Database Error", JOptionPane.ERROR_MESSAGE);
                }
            } else {
                JOptionPane.showMessageDialog(this, "Please enter an order ID", "Input Error", JOptionPane.WARNING_MESSAGE);
            }
    }//GEN-LAST:event_jButton1ActionPerformed

